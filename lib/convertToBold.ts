/**
 * Gets statistics about the text conversion
 * @param text - The input text
 * @param style - The style to check
 * @returns Statistics object with character counts
 */
export function getTextStats(text: string, style: TextStyle = 'bold') {
  const totalChars = text.length;
  const convertibleChars = text.split('').filter(char => isConvertible(char, style)).length;
  const nonConvertibleChars = totalChars - convertibleChars;
  
  return {
    totalChars,
    convertibleChars,
    nonConvertibleChars,
    convertiblePercentage: totalChars > 0 ? Math.round((convertibleChars / totalChars) * 100) : 0
  };
}