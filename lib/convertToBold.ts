export type TextStyle = keyof typeof TEXT_STYLES;

/**
 * Converts regular text to styled Unicode text
 * @param text - The input text to convert
 * @param style - The style to apply
 * @returns The converted styled Unicode text
 */
export function convertToStyle(text: string, style: TextStyle): string {
  if (!text || !TEXT_STYLES[style]) return '';
  
  const styleMap = TEXT_STYLES[style];
  
  return text
    .split('')
    .map(char => styleMap[char] || char)
    .join('');
}

/**
 * Converts regular text to bold Unicode text (backward compatibility)
 * @param text - The input text to convert
 * @returns The converted bold Unicode text
 */
export function convertToBold(text: string): string {
  return convertToStyle(text, 'bold');
}

/**
 * Checks if a character can be converted to the specified style
 * @param char - The character to check
 * @param style - The style to check
 * @returns True if the character can be converted
 */
export function isConvertible(char: string, style: TextStyle): boolean {
  return char in TEXT_STYLES[style];
}

